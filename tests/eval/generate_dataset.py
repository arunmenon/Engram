def _generate_scenario_with_llm(
    spec: dict[str, Any],
    prior_prefixes: dict[str, list[str]],
    model: str,
) -> LLMScenario:
    """Call the LLM to generate a single scenario."""
    import instructor
    import litellm

    client = instructor.from_litellm(litellm.completion)
    prompt = _build_scenario_prompt(spec, prior_prefixes)

    scenario: LLMScenario = client.chat.completions.create(
        model=model,
        response_model=LLMScenario,
        messages=[
            {
                "role": "system",
                "content": (
                    "You are an expert at creating evaluation datasets for context graph "
                    "systems. You generate realistic PayPal-domain scenarios with coherent "
                    "event timelines, entity relationships, and evaluation queries. "
                    "Follow the node ID patterns and edge type constraints exactly."
                ),
            },
            {"role": "user", "content": prompt},
        ],
        max_retries=2,
        temperature=1,
    )

    return scenario


def _validate_scenario(scenario: dict[str, Any], spec: dict[str, Any]) -> list[str]:
    """Validate a post-processed scenario for structural correctness."""
    errors: list[str] = []
    sid = spec["scenario_id"]
    prefix = spec["prefix"]

    # Check node count
    node_count = len(scenario["nodes"])
    target = spec["target_nodes"]
    if node_count < target * 0.6:
        errors.append(f"{sid}: only {node_count} nodes, expected ~{target}")

    # Check all events have correct prefix
    for node in scenario["nodes"]:
        nid = node["node_id"]
        if node["node_type"] == "Event" and not nid.startswith(f"evt-{prefix}-"):
            errors.append(f"{sid}: event {nid} has wrong prefix (expected evt-{prefix}-)")
        if node["node_type"] == "Entity" and not nid.startswith(f"ent-{prefix}-"):
            errors.append(f"{sid}: entity {nid} has wrong prefix (expected ent-{prefix}-)")

    # Check query count
    if len(scenario["queries"]) != 8:
        errors.append(f"{sid}: {len(scenario['queries'])} queries, expected 8")

    # Check intent coverage
    intents_seen = {q["intent"] for q in scenario["queries"]}
    missing = set(INTENT_TYPES) - intents_seen
    if missing:
        errors.append(f"{sid}: missing intents: {missing}")

    # Check queries have enough expected_top_nodes and must_not_appear
    for q in scenario["queries"]:
        if len(q["expected_top_nodes"]) < 3:
            qid = q["query_id"]
            n = len(q["expected_top_nodes"])
            errors.append(f"{sid}/{qid}: only {n} expected_top_nodes, need >= 3")
        if len(q["must_not_appear"]) < 1:
            errors.append(f"{sid}/{q['query_id']}: no must_not_appear entries")

    # Check profile nodes if required
    if spec["has_profile"]:
        node_types = {n["node_type"] for n in scenario["nodes"]}
        if "UserProfile" not in node_types:
            errors.append(f"{sid}: missing UserProfile node (has_profile=true)")
        if "Preference" not in node_types:
            errors.append(f"{sid}: missing Preference nodes (has_profile=true)")
        if "Skill" not in node_types:
            errors.append(f"{sid}: missing Skill nodes (has_profile=true)")

    # Validate FOLLOWS chain exists
    follows_edges = [e for e in scenario["edges"] if e["edge_type"] == "FOLLOWS"]
    if len(follows_edges) < 5:
        errors.append(f"{sid}: only {len(follows_edges)} FOLLOWS edges, need a chain")

    # Validate CAUSED_BY edges exist
    caused_by_edges = [e for e in scenario["edges"] if e["edge_type"] == "CAUSED_BY"]
    if len(caused_by_edges) < 3:
        errors.append(f"{sid}: only {len(caused_by_edges)} CAUSED_BY edges, need >= 4")

    return errors


def generate_dataset(
    model: str,
    output_path: Path,
    dry_run: bool = False,
    deterministic: bool = False,
) -> dict[str, Any]:
    """Generate the complete dataset with 7 new scenarios.

    Args:
        model: LLM model ID for generation (ignored in deterministic mode).
        output_path: Path to write dataset_generated.json.
        dry_run: If True, print prompts but do not call LLM.
        deterministic: If True, use hand-crafted builders instead of LLM.

    Returns:
        The complete dataset dict.
    """
    all_prefixes = dict(EXISTING_PREFIXES)
    generated_scenarios: list[dict[str, Any]] = []
    all_errors: list[str] = []

    for spec in SCENARIO_SPECS:
        sid = spec["scenario_id"]
        prefix = spec["prefix"]

        print(f"\n{'=' * 60}")
        print(f"Generating scenario: {sid}")
        print(f"{'=' * 60}")

        if dry_run:
            prompt = _build_scenario_prompt(spec, all_prefixes)
            print(f"[DRY RUN] Prompt length: {len(prompt)} chars")
            print(f"[DRY RUN] First 500 chars:\n{prompt[:500]}")
            new_prefixes = [f"evt-{prefix}-", f"ent-{prefix}-"]
            if spec["has_profile"]:
                new_prefixes.extend(
                    [
                        f"user-profile-{prefix}-",
                        f"pref-{prefix}-",
                        f"skill-{prefix}-",
                    ]
                )
            all_prefixes[sid] = new_prefixes
            continue

        # Generate scenario
        if deterministic:
            builder = _DETERMINISTIC_BUILDERS[sid]
            llm_scenario = builder(spec)
            print(
                f"  Deterministic: {len(llm_scenario.events)} events, "
                f"{len(llm_scenario.entities)} entities, "
                f"{len(llm_scenario.edges)} edges, "
                f"{len(llm_scenario.queries)} queries"
            )
        else:
            llm_scenario = _generate_scenario_with_llm(spec, all_prefixes, model)
            print(
                f"  LLM returned: {len(llm_scenario.events)} events, "
                f"{len(llm_scenario.entities)} entities, "
                f"{len(llm_scenario.edges)} edges, "
                f"{len(llm_scenario.queries)} queries"
            )

        # Post-process
        processed = _postprocess_scenario(llm_scenario, spec)
        print(f"  Post-processed: {len(processed['nodes'])} nodes")

        # Validate
        errors = _validate_scenario(processed, spec)
        if errors:
            print("  WARNINGS:")
            for err in errors:
                print(f"    - {err}")
            all_errors.extend(errors)
        else:
            print("  Validation: PASSED")

        generated_scenarios.append(processed)

        new_prefixes = [f"evt-{prefix}-", f"ent-{prefix}-"]
        if spec["has_profile"]:
            new_prefixes.extend(
                [
                    f"user-profile-{prefix}-",
                    f"pref-{prefix}-",
                    f"skill-{prefix}-",
                ]
            )
        all_prefixes[sid] = new_prefixes

    if dry_run:
        print(f"\n[DRY RUN] Would generate {len(SCENARIO_SPECS)} scenarios")
        return {}

    # Generate cross-scenario edges
    cross_edges = _generate_cross_scenario_edges(generated_scenarios)
    print(f"\nGenerated {len(cross_edges)} cross-scenario SIMILAR_TO edges")

    # Compute metadata
    total_nodes = sum(len(s["nodes"]) for s in generated_scenarios)
    total_edges = sum(len(s["edges"]) for s in generated_scenarios) + len(cross_edges)
    total_queries = sum(len(s["queries"]) for s in generated_scenarios)

    source = "deterministic" if deterministic else model
    dataset = {
        "generated_at": datetime.now(tz=UTC).isoformat(),
        "model_used": source,
        "scenarios": generated_scenarios,
        "cross_scenario_edges": cross_edges,
        "metadata": {
            "total_nodes": total_nodes,
            "total_edges": total_edges,
            "total_queries": total_queries,
            "scenarios_count": len(generated_scenarios),
            "validation_errors": all_errors,
        },
    }

    # Write to file
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w") as f:
        json.dump(dataset, f, indent=2, default=str)

    print(f"\nDataset written to: {output_path}")
    print(f"  Scenarios: {len(generated_scenarios)}")
    print(f"  Total nodes: {total_nodes}")
    print(f"  Total edges: {total_edges}")
    print(f"  Total queries: {total_queries}")

    if all_errors:
        print(f"\n  VALIDATION WARNINGS ({len(all_errors)}):")
        for err in all_errors:
            print(f"    - {err}")

    return dataset


