class LLMRelevanceJudgment(BaseModel):
    """A single node relevance grade for a query."""

    node_id: str = Field(description="Node ID from this scenario (e.g. evt-rd-060)")
    grade: int = Field(ge=1, le=3, description="3=highly relevant, 2=relevant, 1=marginal")


class LLMEvalQuery(BaseModel):
    """A single evaluation query with ground truth."""

    query_id: str = Field(description="Unique query ID like 'rd-why-01'")
    query: str = Field(description="Natural language question about this scenario")
    intent: str = Field(
        description="One of: why, when, what, related, general, who_is, how_does, personalize",
    )
    expected_top_nodes: list[LLMRelevanceJudgment] = Field(
        min_length=3,
        description="At least 3 nodes with relevance grades",
    )
    must_not_appear: list[str] = Field(
        min_length=1,
        description="Node IDs from OTHER scenarios that should not appear in results",
    )
    rationale: str = Field(description="Why these nodes are relevant, referencing edge types")


class LLMEventNode(BaseModel):
    """An Event node generated by the LLM."""

    node_id: str = Field(description="Event ID like evt-{prefix}-{NNN}")
    event_type: str = Field(
        description="One of: system.session_start, tool.execute, "
        "agent.invoke, observation.output, observation.input, llm.chat",
    )
    description: str = Field(description="What happened in this event")
    importance_score: int | None = Field(default=None, ge=1, le=10, description="1-10 importance")
    status: str = Field(default="success", description="success or failed")


class LLMEntityNode(BaseModel):
    """An Entity node generated by the LLM."""

    node_id: str = Field(description="Entity ID like ent-{prefix}-{name}")
    entity_type: str = Field(description="One of: user, resource, service, agent, concept")
    name: str = Field(description="Human-readable entity name")
    mention_count: int = Field(default=1, ge=1, description="How many times referenced")


class LLMUserProfileNode(BaseModel):
    """A UserProfile node."""

    node_id: str = Field(description="ID like user-profile-{prefix}-{name}")
    name: str = Field(description="Profile display name")


class LLMPreferenceNode(BaseModel):
    """A Preference node."""

    node_id: str = Field(description="ID like pref-{prefix}-{NN}")
    description: str = Field(description="What the user prefers")
    category: str = Field(default="workflow", description="workflow, style, or communication")
    polarity: str = Field(default="positive", description="positive or negative")


class LLMSkillNode(BaseModel):
    """A Skill node."""

    node_id: str = Field(description="ID like skill-{prefix}-{NN}")
    name: str = Field(description="Skill name")
    proficiency_level: float = Field(default=0.85, ge=0.0, le=1.0)


class LLMEdge(BaseModel):
    """An edge in the evaluation graph."""

    source: str = Field(description="Source node ID")
    target: str = Field(description="Target node ID")
    edge_type: str = Field(
        description="One of: FOLLOWS, CAUSED_BY, REFERENCES, SIMILAR_TO, "
        "RELATED_TO, HAS_PROFILE, HAS_PREFERENCE, HAS_SKILL, DERIVED_FROM",
    )
    properties: dict[str, Any] = Field(default_factory=dict)


class LLMScenario(BaseModel):
    """Complete scenario output from the LLM."""

    scenario_id: str
    description: str = Field(description="One-line scenario description")
    events: list[LLMEventNode] = Field(description="Event nodes in chronological order")
    entities: list[LLMEntityNode] = Field(description="Entity nodes referenced by events")
    user_profiles: list[LLMUserProfileNode] = Field(
        default_factory=list,
        description="UserProfile nodes (only for scenarios with has_profile=true)",
    )
    preferences: list[LLMPreferenceNode] = Field(
        default_factory=list,
        description="Preference nodes (only for scenarios with has_profile=true)",
    )
    skills: list[LLMSkillNode] = Field(
        default_factory=list,
        description="Skill nodes (only for scenarios with has_profile=true)",
    )
    edges: list[LLMEdge] = Field(
        description="All edges: FOLLOWS chain, CAUSED_BY, REFERENCES, profile edges, etc.",
    )
    queries: list[LLMEvalQuery] = Field(
        min_length=8,
        max_length=8,
        description="Exactly 8 queries, one per intent type",
    )


