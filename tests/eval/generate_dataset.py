def _deterministic_embedding(seed: str, scenario_offset: int = 0, dim: int = 8) -> list[float]:
    """Generate deterministic 8D embeddings using hash-based seeding.

    Matches the exact algorithm in dataset.py for consistency.
    """
    hash_input = f"{seed}:offset={scenario_offset}"
    hash_bytes = hashlib.sha256(hash_input.encode()).digest()
    embedding = []
    for i in range(dim):
        byte_val = hash_bytes[i % len(hash_bytes)]
        normalized = (byte_val / 127.5) - 1.0
        embedding.append(normalized)
    return embedding


def _postprocess_scenario(
    llm_scenario: LLMScenario,
    spec: dict[str, Any],
) -> dict[str, Any]:
    """Post-process LLM output: overwrite embeddings, timestamps, defaults."""
    scenario_offset = spec["scenario_offset"]
    time_offset = timedelta(hours=spec["time_offset_hours"])
    base_time = _BASE_TIME + time_offset

    nodes: list[dict[str, Any]] = []
    edges: list[dict[str, Any]] = []

    # Process events
    for i, evt in enumerate(llm_scenario.events):
        occurred_at = base_time + timedelta(seconds=i)
        node_id = evt.node_id
        nodes.append(
            {
                "node_id": node_id,
                "node_type": "Event",
                "attributes": {
                    "event_type": evt.event_type,
                    "description": evt.description,
                    "occurred_at": occurred_at.isoformat(),
                    "importance_score": evt.importance_score,
                    "status": evt.status,
                    "embedding": _deterministic_embedding(node_id, scenario_offset),
                    "access_count": 0,
                    "in_degree": 0,
                    "user_affinity": 0.0,
                },
            }
        )

    # Process entities
    for ent in llm_scenario.entities:
        node_id = ent.node_id
        nodes.append(
            {
                "node_id": node_id,
                "node_type": "Entity",
                "attributes": {
                    "entity_type": ent.entity_type,
                    "name": ent.name,
                    "last_seen": _BASE_TIME.isoformat(),
                    "mention_count": ent.mention_count,
                    "embedding": _deterministic_embedding(node_id, scenario_offset),
                },
            }
        )

    # Process user profiles
    for prof in llm_scenario.user_profiles:
        node_id = prof.node_id
        profile_time = base_time + timedelta(seconds=len(llm_scenario.events))
        nodes.append(
            {
                "node_id": node_id,
                "node_type": "UserProfile",
                "attributes": {
                    "name": prof.name,
                    "embedding": _deterministic_embedding(node_id, scenario_offset),
                    "last_seen": profile_time.isoformat(),
                    "mention_count": 5,
                },
            }
        )

    # Process preferences
    for pref in llm_scenario.preferences:
        node_id = pref.node_id
        pref_time = base_time + timedelta(seconds=len(llm_scenario.events))
        nodes.append(
            {
                "node_id": node_id,
                "node_type": "Preference",
                "attributes": {
                    "description": pref.description,
                    "category": pref.category,
                    "polarity": pref.polarity,
                    "embedding": _deterministic_embedding(node_id, scenario_offset),
                    "last_seen": pref_time.isoformat(),
                    "mention_count": 2,
                },
            }
        )

    # Process skills
    for skill in llm_scenario.skills:
        node_id = skill.node_id
        skill_time = base_time + timedelta(seconds=len(llm_scenario.events))
        nodes.append(
            {
                "node_id": node_id,
                "node_type": "Skill",
                "attributes": {
                    "name": skill.name,
                    "proficiency_level": skill.proficiency_level,
                    "embedding": _deterministic_embedding(node_id, scenario_offset),
                    "last_seen": skill_time.isoformat(),
                    "mention_count": 3,
                },
            }
        )

    # Process edges
    for edge in llm_scenario.edges:
        edges.append(
            {
                "source": edge.source,
                "target": edge.target,
                "edge_type": edge.edge_type,
                "properties": edge.properties,
            }
        )

    # Process queries -- set scenario field
    queries: list[dict[str, Any]] = []
    for q in llm_scenario.queries:
        queries.append(
            {
                "query_id": q.query_id,
                "query": q.query,
                "intent": q.intent,
                "scenario": llm_scenario.scenario_id,
                "expected_top_nodes": [
                    {"node_id": j.node_id, "grade": j.grade} for j in q.expected_top_nodes
                ],
                "must_not_appear": q.must_not_appear,
                "rationale": q.rationale,
            }
        )

    return {
        "scenario_id": llm_scenario.scenario_id,
        "description": llm_scenario.description,
        "nodes": nodes,
        "edges": edges,
        "queries": queries,
    }


def _generate_cross_scenario_edges(
    scenarios: list[dict[str, Any]],
) -> list[dict[str, Any]]:
    """Generate cross-scenario SIMILAR_TO edges based on entity type matching.

    Pairs entities of the same entity_type across different scenarios.
    Targets 10-15 edges.
    """
    # Collect entities by type across scenarios
    entities_by_type: dict[str, list[tuple[str, str]]] = {}  # type -> [(node_id, scenario_id)]
    for scenario in scenarios:
        sid = scenario["scenario_id"]
        for node in scenario["nodes"]:
            if node["node_type"] == "Entity":
                etype = node["attributes"].get("entity_type", "unknown")
                entities_by_type.setdefault(etype, []).append((node["node_id"], sid))

    cross_edges: list[dict[str, Any]] = []
    similarity_reasons = {
        "service": "similar_service_roles",
        "resource": "similar_resource_types",
        "agent": "similar_agent_roles",
        "user": "similar_user_accounts",
        "concept": "related_concepts",
    }

    for etype, entity_list in entities_by_type.items():
        # Only pair across different scenarios
        by_scenario: dict[str, list[str]] = {}
        for node_id, sid in entity_list:
            by_scenario.setdefault(sid, []).append(node_id)

        scenario_ids = list(by_scenario.keys())
        for i in range(len(scenario_ids)):
            for j in range(i + 1, len(scenario_ids)):
                sid_a = scenario_ids[i]
                sid_b = scenario_ids[j]
                # Take first entity from each scenario for this type
                node_a = by_scenario[sid_a][0]
                node_b = by_scenario[sid_b][0]

                # Deterministic similarity based on hash
                pair_hash = hashlib.sha256(f"{node_a}:{node_b}".encode()).digest()
                similarity = 0.5 + (pair_hash[0] / 255.0) * 0.4  # Range: 0.5-0.9

                cross_edges.append(
                    {
                        "source": node_a,
                        "target": node_b,
                        "edge_type": "SIMILAR_TO",
                        "properties": {
                            "similarity": round(similarity, 2),
                            "reason": similarity_reasons.get(etype, "entity_type_match"),
                        },
                    }
                )

    # Limit to 15 edges, sorted by similarity descending
    cross_edges.sort(key=lambda e: e["properties"]["similarity"], reverse=True)
    return cross_edges[:15]


